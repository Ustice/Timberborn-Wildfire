const timestampSlug = (date: Date): string => date.toISOString().replace(/[:.]/gu, "-");

const defaultOutputDir = (name: string): string => join(generatedScenariosRoot, `${name}-${timestampSlug(new Date())}`);

const validateSlug = (name: string): string => {
  if (!/^[a-z0-9][a-z0-9-]{0,79}$/u.test(name)) {
    fail(`Invalid --name "${name}". Use a lowercase slug with only a-z, 0-9, and hyphen, starting with a letter or number.`);
  }

  return name;
};

const parseArgs = (args: string[]): Options => {
  const options: Options = {
    allowUserSaveOutput: false,
    dryRun: false,
    help: false,
    mutateEntities: true,
    name: "wildfire-world-consequence-scenario",
    outputDir: "",
    overwrite: false,
    templatePath: null,
  };
  let skipNext = false;

  args.reduce((_, arg, index) => {
    if (skipNext) {
      skipNext = false;
      return undefined;
    }

    if (arg === "--allow-user-save-output") {
      options.allowUserSaveOutput = true;
    } else if (arg === "--dry-run") {
      options.dryRun = true;
    } else if (arg === "--help" || arg === "-h") {
      options.help = true;
    } else if (arg === "--name") {
      options.name = requireValue(args, index + 1, arg);
      skipNext = true;
    } else if (arg.startsWith("--name=")) {
      options.name = arg.slice("--name=".length);
    } else if (arg === "--no-entity-mutation") {
      options.mutateEntities = false;
    } else if (arg === "--output-dir") {
      options.outputDir = resolve(requireValue(args, index + 1, arg));
      skipNext = true;
    } else if (arg.startsWith("--output-dir=")) {
      options.outputDir = resolve(arg.slice("--output-dir=".length));
    } else if (arg === "--overwrite") {
      options.overwrite = true;
    } else if (arg === "--template") {
      options.templatePath = resolve(requireValue(args, index + 1, arg));
      skipNext = true;
    } else if (arg.startsWith("--template=")) {
      options.templatePath = resolve(arg.slice("--template=".length));
    } else {
      fail(`Unknown argument: ${arg}`);
    }

    return undefined;
  }, undefined);

  options.name = validateSlug(options.name);
  options.outputDir = resolve(options.outputDir === "" ? defaultOutputDir(options.name) : options.outputDir);

  return options;
};

