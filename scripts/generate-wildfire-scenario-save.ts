const isSameOrInside = (path: string, parent: string): boolean => {
  const child = resolve(path);
  const root = resolve(parent);
  const diff = relative(root, child);

  return diff === "" || (!diff.startsWith("..") && !diff.startsWith("/") && diff !== "..");
};

const isChildPath = (path: string, parent: string): boolean => {
  const diff = relative(resolve(parent), resolve(path));

  return diff !== "" && !diff.startsWith("..") && !diff.startsWith("/") && diff !== "..";
};

const assertPathInside = (path: string, parent: string, label: string): string => {
  const resolvedPath = resolve(path);
  if (!isChildPath(resolvedPath, parent)) {
    fail(`${label} must stay inside the output directory. Refusing path: ${resolvedPath}`);
  }

  return resolvedPath;
};

const assertNoExistingSymlink = (path: string, label: string): void => {
  if (existsSync(path) && lstatSync(path).isSymbolicLink()) {
    fail(`${label} must not be a symbolic link: ${path}`);
  }
};

const nearestExistingParent = (path: string): string => {
  let current = resolve(path);
  while (!existsSync(current)) {
    const parent = dirname(current);
    if (parent === current) {
      fail(`Could not find an existing parent directory for ${path}.`);
    }
    current = parent;
  }

  return current;
};

const existingAncestorsFrom = (root: string, path: string): string[] => {
  const resolvedRoot = resolve(root);
  const resolvedPath = resolve(path);
  const segments = relative(resolvedRoot, resolvedPath)
    .split("/")
    .filter((segment) => segment.length > 0);

  return segments.reduce<string[]>(
    (ancestors, segment) => {
      const next = join(ancestors.at(-1) ?? resolvedRoot, segment);
      return existsSync(next) ? [...ancestors, next] : ancestors;
    },
    [resolvedRoot],
  );
};

const assertExistingDirectory = (path: string, label: string): void => {
  if (!existsSync(path)) {
    return;
  }
  if (!lstatSync(path).isDirectory()) {
    fail(`${label} exists but is not a directory: ${path}`);
  }
};

const assertNoSymlinkedExistingDirectory = (path: string): void => {
  assertNoExistingSymlink(path, "Output folder");
  assertExistingDirectory(path, "Output path");
};

const isClearlyGeneratedScenarioOutputDir = (outputDir: string): boolean => {
  const normalized = resolve(outputDir);
  const segments = normalized.split("/");
  const finalSegment = segments.at(-1) ?? "";

  return isChildPath(normalized, generatedScenariosRoot) && finalSegment.length > 0;
};

const validateOutputDirSafety = (outputDir: string): string => {
  const resolvedOutputDir = resolve(outputDir);
  const dangerousRoots = [
    resolve("/"),
    resolve(home),
    resolve(home, "Documents"),
    resolve(home, "Documents", "Timberborn"),
    resolve(home, "Documents", "Timberborn", "ExperimentalSaves"),
    resolve(home, "Documents", "Timberborn", "Maps"),
  ];
  const matchingDangerousRoot = dangerousRoots.find((root) => resolvedOutputDir === root);
  if (matchingDangerousRoot) {
    fail(`Refusing dangerous output directory: ${matchingDangerousRoot}`);
  }
  if (isSameOrInside(resolvedOutputDir, resolve(home, "Documents", "Timberborn"))) {
    fail(`Refusing to write generated scenario output under Documents/Timberborn save roots: ${resolvedOutputDir}`);
  }
  if (!isClearlyGeneratedScenarioOutputDir(resolvedOutputDir)) {
    fail(
      `Refusing output directory that is not clearly a generated Wildfire scenario folder: ${resolvedOutputDir}. Use a folder under ${join(
        generatedScenariosRoot,
        "<scenario-name>",
      )}.`,
    );
  }

  return resolvedOutputDir;
};

const assertRealParentChainInsideGeneratedRoot = (outputDir: string): void => {
  if (!existsSync(generatedScenariosRoot)) {
    const rootParent = nearestExistingParent(dirname(generatedScenariosRoot));
    assertNoExistingSymlink(rootParent, "Generated scenario root parent");
    assertExistingDirectory(rootParent, "Generated scenario root parent");
    const realRootParent = realpathSync(rootParent);
    if (realRootParent !== resolve(rootParent)) {
      fail(`Generated scenario root parent must not resolve through a symlink: ${rootParent} -> ${realRootParent}`);
    }
    mkdirSync(generatedScenariosRoot, { recursive: true });
  }

  assertNoExistingSymlink(generatedScenariosRoot, "Generated scenario root");
  assertExistingDirectory(generatedScenariosRoot, "Generated scenario root");
  const realGeneratedRoot = realpathSync(generatedScenariosRoot);
  if (realGeneratedRoot !== resolve(generatedScenariosRoot)) {
    fail(`Generated scenario root must not be a symbolic link: ${generatedScenariosRoot} -> ${realGeneratedRoot}`);
  }

  const existingAncestors = existingAncestorsFrom(generatedScenariosRoot, outputDir);
  existingAncestors.forEach((ancestor) => {
    assertNoExistingSymlink(ancestor, "Output ancestor");
    assertExistingDirectory(ancestor, "Output ancestor");
    const realAncestor = realpathSync(ancestor);
    if (!isSameOrInside(realAncestor, realGeneratedRoot)) {
      fail(`Output ancestor escapes generated scenario root: ${ancestor} -> ${realAncestor}`);
    }
  });

  const nearestParent = nearestExistingParent(dirname(outputDir));
  const realNearestParent = realpathSync(nearestParent);
  if (!isSameOrInside(realNearestParent, realGeneratedRoot)) {
    fail(`Output parent escapes generated scenario root: ${nearestParent} -> ${realNearestParent}`);
  }
};

const readManifestMarker = (manifestPath: string): string | null => {
  if (!existsSync(manifestPath)) {
    return null;
  }

  try {
    const manifest = JSON.parse(readFileSync(manifestPath, "utf8")) as JsonValue;
    return asString(getPath(manifest, ["generator", "tool"]));
  } catch {
    return fail(`Refusing --overwrite because ${manifestPath} is not readable generated-scenario JSON.`);
  }
};

const assertExistingOutputIsGeneratedByThisTool = (outputDir: string, manifestPath: string): void => {
  if (!existsSync(outputDir)) {
    return;
  }
  if (!existsSync(manifestPath)) {
    fail(`Refusing --overwrite because no prior ${manifestFileName} marker exists in ${outputDir}.`);
  }

  const marker = readManifestMarker(manifestPath);
  if (marker !== generatorTool) {
    fail(`Refusing --overwrite because ${manifestPath} is not marked as generated by ${generatorTool}.`);
  }
};

const outputPathsFor = (options: Options): { archivePath: string; manifestPath: string; outputDir: string } => {
  const outputDir = validateOutputDirSafety(options.outputDir);
  const archivePath = assertPathInside(resolve(outputDir, `${options.name}.timber`), outputDir, "Archive path");
  const manifestPath = assertPathInside(resolve(outputDir, manifestFileName), outputDir, "Manifest path");

  return { archivePath, manifestPath, outputDir };
};

const assertFinalPathConfinement = (outputDir: string, archivePath: string, manifestPath: string): void => {
  const realGeneratedRoot = realpathSync(generatedScenariosRoot);
  const realOutputDir = realpathSync(outputDir);
  if (!isSameOrInside(realOutputDir, realGeneratedRoot)) {
    fail(`Output folder escapes generated scenario root after creation: ${outputDir} -> ${realOutputDir}`);
  }
  assertPathInside(archivePath, outputDir, "Archive path");
  assertPathInside(manifestPath, outputDir, "Manifest path");
};

const prepareOutputDir = (outputDir: string, archivePath: string, manifestPath: string, overwrite: boolean): void => {
  assertRealParentChainInsideGeneratedRoot(outputDir);
  assertNoExistingSymlink(outputDir, "Output folder");
  assertNoExistingSymlink(manifestPath, "Manifest path");
  assertNoSymlinkedExistingDirectory(outputDir);

  if (existsSync(outputDir) && !overwrite) {
    fail(`Output folder already exists. Use --overwrite to replace it: ${outputDir}`);
  }
  if (existsSync(outputDir)) {
    assertExistingOutputIsGeneratedByThisTool(outputDir, manifestPath);
    rmSync(outputDir, { recursive: true });
  }

  mkdirSync(outputDir, { recursive: true });
  assertFinalPathConfinement(outputDir, archivePath, manifestPath);
};

const run = (): void => {
  const options = parseArgs(Bun.argv.slice(2));
  if (options.help) {
    console.log(usage);
    return;
  }
  const templatePath = options.templatePath ?? fail("--template is required.");
  if (!existsSync(templatePath)) {
    fail(`Template archive does not exist: ${templatePath}`);
  }

  const generatedAtDate = new Date();
  const generatedAt = generatedAtDate.toISOString();
  const paths = outputPathsFor(options);
  const inspection = inspectArchive(templatePath);
  const world = inspection.jsonFiles["world.json"];
  const mutation = options.mutateEntities && world ? mutateWorldEntities(world) : { blockers: [], generated: [], nextWorld: world };
  const metadata = updateMetadata(inspection.jsonFiles["save_metadata.json"], generatedAtDate);
  const schemaBlockers = buildSchemaBlockers(inspection, world, metadata, mutation.blockers);
  const manifest = buildManifest(options, inspection, generatedAt, paths.archivePath, paths.manifestPath, mutation.generated, mutation.blockers, schemaBlockers);

  log(`template=${templatePath}`);
  log(`output_dir=${paths.outputDir}`);
  log(`archive_entries=${inspection.entries.map((entry) => entry.name).join(",")}`);
  log(`json_files=${Object.keys(inspection.jsonFiles).join(",")}`);
  log(`map_size=${manifest.template.mapSize.x ?? "unknown"}x${manifest.template.mapSize.y ?? "unknown"}`);
  log(`planned_counts=${JSON.stringify(manifest.plan.expectedEntityCountsByCategory)}`);
  log(`generated_counts=${JSON.stringify(manifest.result.generatedEntityCountsByCategory)}`);
  log(`schema_blockers=${schemaBlockers.length}`);

  if (options.dryRun) {
    log(`dry_run=true would_write_archive=${paths.archivePath}`);
    log(`dry_run=true would_write_manifest=${paths.manifestPath}`);
    console.log(JSON.stringify(manifest, null, 2));
    return;
  }

  prepareOutputDir(paths.outputDir, paths.archivePath, paths.manifestPath, options.overwrite);

  const entriesWithWorld =
    world && mutation.nextWorld
      ? replaceEntry(inspection.entries, "world.json", Buffer.from(JSON.stringify(mutation.nextWorld), "utf8"))
      : inspection.entries;
  const entriesWithMetadata = metadata
    ? replaceEntry(entriesWithWorld, "save_metadata.json", Buffer.from(JSON.stringify(metadata), "utf8"))
    : entriesWithWorld;

  writeZip(paths.archivePath, entriesWithMetadata);
  writeFileSync(paths.manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);
  log(`wrote_archive=${paths.archivePath}`);
  log(`wrote_manifest=${paths.manifestPath}`);
};

if (import.meta.main) {
  try {
    run();
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exit(1);
  }
}
