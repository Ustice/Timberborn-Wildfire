const viewOf = (buffer: Buffer): DataView => new DataView(buffer.buffer, buffer.byteOffset, buffer.byteLength);

const readUtf8 = (buffer: Buffer, offset: number, length: number): string => buffer.subarray(offset, offset + length).toString("utf8");

const findEndOfCentralDirectory = (bytes: Buffer): number => {
  let offset = bytes.length - 22;
  while (offset >= 0) {
    if (viewOf(bytes).getUint32(offset, true) === 0x06054b50) {
      return offset;
    }
    offset -= 1;
  }

  return fail("Could not find ZIP end-of-central-directory record.");
};

const readZip = (path: string): ZipEntry[] => {
  const bytes = readFileSync(path);
  const view = viewOf(bytes);
  const eocdOffset = findEndOfCentralDirectory(bytes);
  const entryCount = view.getUint16(eocdOffset + 10, true);
  const centralDirectoryOffset = view.getUint32(eocdOffset + 16, true);

  return Array.from({ length: entryCount }).reduce<{ entries: ZipEntry[]; offset: number }>(
    (state) => {
      if (view.getUint32(state.offset, true) !== 0x02014b50) {
        fail(`Invalid ZIP central directory signature at offset ${state.offset}.`);
      }

      const compressionMethod = view.getUint16(state.offset + 10, true);
      const compressedSize = view.getUint32(state.offset + 20, true);
      const uncompressedSize = view.getUint32(state.offset + 24, true);
      const fileNameLength = view.getUint16(state.offset + 28, true);
      const extraLength = view.getUint16(state.offset + 30, true);
      const commentLength = view.getUint16(state.offset + 32, true);
      const localHeaderOffset = view.getUint32(state.offset + 42, true);
      const name = readUtf8(bytes, state.offset + 46, fileNameLength);

      if (view.getUint32(localHeaderOffset, true) !== 0x04034b50) {
        fail(`Invalid ZIP local header signature for ${name}.`);
      }

      const localNameLength = view.getUint16(localHeaderOffset + 26, true);
      const localExtraLength = view.getUint16(localHeaderOffset + 28, true);
      const dataOffset = localHeaderOffset + 30 + localNameLength + localExtraLength;
      const compressedData = bytes.subarray(dataOffset, dataOffset + compressedSize);
      const data =
        compressionMethod === 0
          ? Buffer.from(compressedData)
          : compressionMethod === 8
            ? inflateRawSync(compressedData)
            : fail(`Unsupported ZIP compression method ${compressionMethod} for ${name}.`);

      if (data.length !== uncompressedSize) {
        fail(`Uncompressed size mismatch for ${name}: expected ${uncompressedSize}, got ${data.length}.`);
      }

      return {
        entries: [...state.entries, { compressedSize, compressionMethod, data, name, uncompressedSize }],
        offset: state.offset + 46 + fileNameLength + extraLength + commentLength,
      };
    },
    { entries: [], offset: centralDirectoryOffset },
  ).entries;
};

const crcTable = Array.from({ length: 256 }, (_, index) =>
  Array.from({ length: 8 }).reduce(
    (crc) => ((crc as number) & 1 ? 0xedb88320 ^ ((crc as number) >>> 1) : (crc as number) >>> 1),
    index,
  ) as number,
);

const crc32 = (data: Buffer): number =>
  data.reduce((crc, byte) => (crcTable[(crc ^ byte) & 0xff] ?? 0) ^ (crc >>> 8), 0xffffffff) ^ 0xffffffff;

const dosDateTime = (date: Date): { date: number; time: number } => ({
  date: ((date.getFullYear() - 1980) << 9) | ((date.getMonth() + 1) << 5) | date.getDate(),
  time: (date.getHours() << 11) | (date.getMinutes() << 5) | Math.floor(date.getSeconds() / 2),
});

const writeZip = (path: string, entries: ZipEntry[]): void => {
  const now = dosDateTime(new Date());
  const localRecords = entries.reduce<{ chunks: Buffer[]; central: Buffer[]; offset: number }>(
    (state, entry) => {
      const name = Buffer.from(entry.name, "utf8");
      const compressedData = deflateRawSync(entry.data);
      const checksum = crc32(entry.data) >>> 0;
      const localHeader = Buffer.alloc(30 + name.length);
      const centralHeader = Buffer.alloc(46 + name.length);

      localHeader.writeUInt32LE(0x04034b50, 0);
      localHeader.writeUInt16LE(20, 4);
      localHeader.writeUInt16LE(0x0800, 6);
      localHeader.writeUInt16LE(8, 8);
      localHeader.writeUInt16LE(now.time, 10);
      localHeader.writeUInt16LE(now.date, 12);
      localHeader.writeUInt32LE(checksum, 14);
      localHeader.writeUInt32LE(compressedData.length, 18);
      localHeader.writeUInt32LE(entry.data.length, 22);
      localHeader.writeUInt16LE(name.length, 26);
      localHeader.writeUInt16LE(0, 28);
      name.copy(localHeader, 30);

      centralHeader.writeUInt32LE(0x02014b50, 0);
      centralHeader.writeUInt16LE(20, 4);
      centralHeader.writeUInt16LE(20, 6);
      centralHeader.writeUInt16LE(0x0800, 8);
      centralHeader.writeUInt16LE(8, 10);
      centralHeader.writeUInt16LE(now.time, 12);
      centralHeader.writeUInt16LE(now.date, 14);
      centralHeader.writeUInt32LE(checksum, 16);
      centralHeader.writeUInt32LE(compressedData.length, 20);
      centralHeader.writeUInt32LE(entry.data.length, 24);
      centralHeader.writeUInt16LE(name.length, 28);
      centralHeader.writeUInt16LE(0, 30);
      centralHeader.writeUInt16LE(0, 32);
      centralHeader.writeUInt16LE(0, 34);
      centralHeader.writeUInt16LE(0, 36);
      centralHeader.writeUInt32LE(0, 38);
      centralHeader.writeUInt32LE(state.offset, 42);
      name.copy(centralHeader, 46);

      return {
        chunks: [...state.chunks, localHeader, compressedData],
        central: [...state.central, centralHeader],
        offset: state.offset + localHeader.length + compressedData.length,
      };
    },
    { central: [], chunks: [], offset: 0 },
  );
  const centralDirectory = Buffer.concat(localRecords.central);
  const eocd = Buffer.alloc(22);
  eocd.writeUInt32LE(0x06054b50, 0);
  eocd.writeUInt16LE(entries.length, 8);
  eocd.writeUInt16LE(entries.length, 10);
  eocd.writeUInt32LE(centralDirectory.length, 12);
  eocd.writeUInt32LE(localRecords.offset, 16);
  eocd.writeUInt16LE(0, 20);

  writeFileSync(path, Buffer.concat([...localRecords.chunks, centralDirectory, eocd]));
};

const inspectArchive = (templatePath: string): ArchiveInspection => {
  const entries = readZip(templatePath);
  const jsonFiles = entries
    .filter((entry) => entry.name.endsWith(".json"))
    .reduce<Record<string, JsonValue>>((files, entry) => ({ ...files, [entry.name]: JSON.parse(entry.data.toString("utf8")) as JsonValue }), {});

  return { entries, jsonFiles };
};

